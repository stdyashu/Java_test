package variable_001.m020;

public class Fast {

	public static void main(String[] args) {
		// 変数を宣言し初期値を設定
        int x = 10;
        
        // xの値をyにコピー
        int y = x;
        
        // xとyの値を表示
        System.out.println("変数 x の値は: " + x);
        System.out.println("変数 y の値は: " + y);
	}

}
