package variable_001.m022;

public class Fast {

	public static void main(String[] args) {
		// 変数を宣言し初期値を設定
        String firstName = "John";
        String lastName = "Doe";
        
        // フルネームを結合
        String fullName = firstName + " " + lastName;
        
        // フルネームを表示
        System.out.println("フルネーム: " + fullName);
	}

}
